function scheduleDataNormalization(normalizedObject) {
  var schedule = [];
  var scheduleObject = {};
  for(key in normalizedObject) {
    if(!isNaN(key) && normalizedObject[key].hasOwnProperty('date')) {
      schedule.push(normalizedObject[key]);
    } else {
      scheduleObject[key] = normalizedObject[key];
    }
  }
  scheduleObject.schedule = schedule;
  return scheduleObject;
}

